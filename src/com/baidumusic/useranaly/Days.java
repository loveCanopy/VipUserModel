package com.baidumusic.useranaly;

import java.util.Date;

public class Days {
	private String id;
	private String title;
	private Date date;

	public Days(String id, String title, Date date) {
		this.id = id;
		this.title = title;
		this.date = date;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

}